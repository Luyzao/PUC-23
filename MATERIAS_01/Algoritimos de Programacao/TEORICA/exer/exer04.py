#Ler 2 numeros inteiros diferentes, imprimir qual o maior e qual o menor

a = int(input("Digite o 1 nummero inteiro: "))
b = int(input("Digite o 2 numero inteiro: "))

if a>b:
    print(f"o primeiro numero {a} é maior que {b} o segundo")
else:
    print(f"o segundo numero{b} é maior que o {a} primeiro")